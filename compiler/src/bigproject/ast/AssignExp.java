package bigproject.ast;

public class AssignExp extends Exp {

	public Exp left;
	public int op;
	public Exp right;
	
	public AssignExp(Exp a, int b, Exp c) {
		left = a;
		op = b;
		right = c;
	}
	
	public static int Assign = 0,        // '='
					  MultAssign = 1,    // '*='
					  DivAssign = 2,     // '/='
					  ModAssign = 3,     // '%='
					  PlusAssign = 4,    // '+='
					  MinusAssign = 5,   // '-='
					  ShlAssign = 6,     // '<<='
					  ShrAssign = 7,     // '>>='
					  AndAssign = 8,     // '&='
					  XorAssign = 9,     // '^='
					  OrAssign = 10;     // '|='

}
