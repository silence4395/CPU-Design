package bigproject.ast;

public class ForStat extends Stat {

	public Exp firstcond;
	public Exp secondcond;
	public Exp thirdcond;
	public Stat body;
	public Boolean unrollable = true;
	
	public ForStat() {
		firstcond = null;
		secondcond = null;
		thirdcond = null;
		body = null;
	}
	
	public void setFirstCond(Exp a) {
		firstcond = a;
	}
	
	public void setSecondCond(Exp a) {
		secondcond = a;
	}
	
	public void setThirdCond(Exp a) {
		thirdcond = a;
	}
	
	public void setBody(Stat a) {
		body = a;
	}
}
